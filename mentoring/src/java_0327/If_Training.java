package java_0327;

public class If_Training {
    public static void main(String[] args) {
        String s = "박성수";
        if(s == "박성수"){
            System.out.println("변수 s는 박성수 입니다.");
        }else {
            System.out.println("변수 s는 박성수가 아닙니다.");
        }

        double d = 3.14;
        if(d == 3.14){
            System.out.println(d);
        }
    }
}
